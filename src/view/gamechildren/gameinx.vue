<template>
  <div>
    <div class="title">用户管理-游戏</div>
    <div class="content">
      <div>
        <el-form :inline="true" :model="formData" ref="formData" class="form-inline">
          <el-form-item label="用户昵称" prop="nickname" style="width:280px;">
            <el-input v-model="formData.nickname" placeholder="请输入您要查询的昵称"></el-input>
          </el-form-item>
          <el-form-item>
            <el-button type="primary" @click="onSubmit">搜索</el-button>
            <el-button @click="resetForm('formData')">重置</el-button>
          </el-form-item>
        </el-form>
      </div>
      <div class="tableBox">
        <el-table :data="tableData" border>
          <el-table-column prop="Id" label="用户ID"></el-table-column>
          <el-table-column prop="nick_name" label="用户昵称"></el-table-column>
          <el-table-column label="用户头像">
            <template slot-scope="scope">
              <img :src="scope.row.figureurl" class="head_pic">
            </template>
          </el-table-column>
          <el-table-column prop="city" label="地区"></el-table-column>
          <el-table-column prop="kill" label="用户状态"></el-table-column>
          <el-table-column prop="create_time" label="创建时间"></el-table-column>
          <el-table-column label="操作">
            <template slot-scope="scope">
              <el-button
                @click="handleClick(scope.row.Id,scope.row.kill)"
                type="text"
                size="small"
              >封杀</el-button>
              <!--<router-link to="/rulemodify">
                <el-button type="text" size="small">统计</el-button>
              </router-link>-->
            </template>
          </el-table-column>
        </el-table>
      </div>
      <div class="pagebox" v-if="tableData.length>0" >
        <el-pagination
          @size-change="handleSizeChange"
          @current-change="handleCurrentChange"
          :current-page="formData.page"
          :page-sizes="[10, 20, 30, 40]"
          :page-size="formData.page_size"
          layout="total, sizes, prev, pager, next, jumper"
          :total="count"
        ></el-pagination>
      </div>
    </div>
  </div>
</template>

<script>
import { gamekill,gameuser,searchName} from "@/api/index";
export default {
  name: "goodsAdmin",
  props:['id'],
  data() {
    return {
      formData: {
        user_id: "",
        nickname: "",
        user_pid: "",
        use_state: "",
        slot_time: "",
        page: 1,
        pageSize: 10
      },
      count: 0,
      multipleSelection: "",
      GoodData: {
        id: "",
        time: ""
      },
      tableData: []
    };
  },
  mounted() {

  },
  created() {
    this.load();
  },
  methods: {
//    拉取用户数据
    load() {
      var data = this.formData;
      data.activityId= localStorage.getItem("box_id");
      gameuser(data).then(res => {
        console.log(res.data.info);
          this.tableData = res.data.info;
          this.count = parseInt(res.data.count);
      }).catch(err=>{
        console.log(err)
      });
    },
    //封杀
    handleClick(id, status) {
      var data = {
          box_id: localStorage.getItem("box_id"),
          id: id,
          status: status
        };
      if(data.status==0){
        this.$confirm("确定封杀该用户, 是否继续?", "提示", {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning"
        }).then(() => {
          gamekill(data).then(res => {
            console.log(res);
            this.$message({
              type: "success",
              message: "修改成功!"
            });
          }).catch(err=>{
            console.log(err)
          });
        });
      }else{
        this.$confirm("该用户已封杀", "提示", {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning"
        })
      }

    },
    //search搜索
    onSubmit() {
      var data = this.formData;
      data.activityId= localStorage.getItem("box_id");
      if(!!data.nickname){
        searchName(data).then(res => {
          console.log(res.data.info);
          this.tableData = res.data.info;
          this.count = parseInt(res.data.count);
        }).catch(err=>{
          console.log(err)
        });
      }
    },
    //重置表单
    resetForm(formName) {
      this.$refs[formName].resetFields();
    },
    handleSizeChange(val) {
      this.formData.page_size = val;
      this.load();
    },
    handleCurrentChange(val) {
      this.formData.page = val;
      this.load();
    }
  }
};
</script>

